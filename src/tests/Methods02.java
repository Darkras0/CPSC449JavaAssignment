package tests;

public class Methods02 {
	private static int add(int x, int y) {
		return x + y;
	}
	
	private static float add(float x, float y) {
		return x + y;
	}
	
	private static int length(String arg) {
		return arg.length();
	}
	
	private static String charAt(String arg, int x) {
		return "" + arg.charAt(x);
	}
}
